package com.sway;

public class Products {

    private String product_id;
    private String product_catalog_id;
    private String product_name;
    private int list_price;

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getProduct_catalog_id() {
        return product_catalog_id;
    }

    public void setProduct_catalog_id(String product_catalog_id) {
        this.product_catalog_id = product_catalog_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public int getList_price() {
        return list_price;
    }

    public void setList_price(int list_price) {
        this.list_price = list_price;
    }
}
